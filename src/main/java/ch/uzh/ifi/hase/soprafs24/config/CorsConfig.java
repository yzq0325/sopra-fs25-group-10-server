package ch.uzh.ifi.hase.soprafs24.config;

import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;
import org.springframework.web.servlet.config.annotation.CorsRegistry;
import org.springframework.web.servlet.config.annotation.WebMvcConfigurer;

@Configuration
public class CorsConfig {

    @Bean
    public WebMvcConfigurer corsConfigurer() {
        return new WebMvcConfigurer() {
            @Override
            public void addCorsMappings(CorsRegistry registry) {
                registry.addMapping("/**")
                        .allowedOrigins("http://localhost:3000", "https://sopra-fs25-group-10-client-teal.vercel.app", "https://sopra-fs25-group-10-client.vercel.app") // or wherever your frontend runs
                        .allowedMethods("*")
                        .allowCredentials(true); // important for cookies or sessions
            }
        };
    }
}
